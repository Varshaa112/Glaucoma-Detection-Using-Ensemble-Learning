# ============ ENHANCED LIME FUNCTION ============
def explain_with_lime_enhanced(img_path, model, model_name):
    """Generate LIME explanation with enhanced visualization"""

    # Load and preprocess image
    img = cv2.imread(img_path)
    img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
    img = cv2.resize(img, (224, 224))

    # Get prediction
    img_array = np.expand_dims(img, axis=0) / 255.0
    pred = model.predict(img_array, verbose=0)[0][0]
    label = "GLAUCOMA" if pred > 0.5 else "NORMAL"

    # Define prediction function for LIME
    def predict_fn(images):
        images = images.astype(np.float32) / 255.0
        preds = model.predict(images, verbose=0)
        return np.hstack([1-preds, preds])

    # Get explanation with more samples for better accuracy
    explanation = explainer.explain_instance(
        img.astype(np.float64),
        predict_fn,
        top_labels=2,
        num_samples=1000,  # Increased for better accuracy
        random_seed=42
    )

    # Get the explanation for the predicted class
    temp, mask = explanation.get_image_and_mask(
        explanation.top_labels[0],
        positive_only=True,
        num_features=10,
        hide_rest=False
    )

    # Get both positive and negative explanations
    temp2, mask2 = explanation.get_image_and_mask(
        explanation.top_labels[0],
        positive_only=False,
        num_features=10,
        hide_rest=False
    )

    # Calculate green area (supports prediction)
    green_area = calculate_green_area(mask)

    return img, label, pred, temp, mask, temp2, mask2, green_area

