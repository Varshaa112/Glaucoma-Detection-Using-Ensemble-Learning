# Function to copy images with label
def copy_images_with_label(source_folder, label, dest_train, dest_test, split=0.8):
    if not os.path.exists(source_folder):
        print(f"   ❌ Folder not found: {source_folder}")
        return 0

    images = [f for f in os.listdir(source_folder)
              if f.lower().endswith(('.jpg', '.png', '.jpeg'))]

    print(f"   📂 Found {len(images)} images in {os.path.basename(source_folder)}")

    if len(images) == 0:
        return 0

    # Shuffle and split
    random.shuffle(images)
    split_idx = int(len(images) * split)

    train_images = images[:split_idx]
    test_images = images[split_idx:]

    # Copy to train
    for img in tqdm(train_images, desc=f"Copying {label} train"):
        src = os.path.join(source_folder, img)
        dst = os.path.join(dest_train, f"{label}_{img}")
        shutil.copy2(src, dst)

    # Copy to test
    for img in tqdm(test_images, desc=f"Copying {label} test"):
        src = os.path.join(source_folder, img)
        dst = os.path.join(dest_test, f"{label}_{img}")
        shutil.copy2(src, dst)

    return len(images)

