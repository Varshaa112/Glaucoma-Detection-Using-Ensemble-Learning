def preprocess_image(image_path, target_size=(224, 224)):
    """Simple but effective preprocessing"""
    img = cv2.imread(image_path)
    if img is None:
        return None
    img = cv2.resize(img, target_size)
    img_rgb = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
    img_normalized = img_rgb / 255.0
    return img_normalized

def process_dataset(source_folder, dest_folder, class_name):
    """Process all images in a folder"""
    source_path = os.path.join(source_folder, class_name)
    dest_path = os.path.join(dest_folder, class_name)

    if not os.path.exists(source_path):
        print(f"❌ Not found: {source_path}")
        return 0

    images = [f for f in os.listdir(source_path)
              if f.lower().endswith(('.png', '.jpg', '.jpeg'))]

    print(f"\n📋 Processing {len(images)} images from {class_name}...")

    processed = 0
    for i, img_file in enumerate(images):
        img_path = os.path.join(source_path, img_file)
        processed_img = preprocess_image(img_path)

        if processed_img is not None:
            save_path = os.path.join(dest_path, img_file)
            plt.imsave(save_path, processed_img)
            processed += 1

            if (i + 1) % 1000 == 0:
                print(f"   ✅ Processed {i + 1}/{len(images)} images")

    print(f"   ✅ Done! Processed {processed} images")
    return processed

